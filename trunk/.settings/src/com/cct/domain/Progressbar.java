package com.cct.domain;

public class Progressbar {
	
	private int progress;
	private boolean flagProcess;
	private String progressTxt;

	public int getProgress() {
		return progress;
	}

	public void setProgress(int progress) {
		this.progress = progress;
	}

	public boolean isFlagProcess() {
		return flagProcess;
	}

	public void setFlagProcess(boolean flagProcess) {
		this.flagProcess = flagProcess;
	}

	public String getProgressTxt() {
		return progressTxt;
	}

	public void setProgressTxt(String progressTxt) {
		this.progressTxt = progressTxt;
	}

}
