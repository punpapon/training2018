package com.cct.trn.core.dialog.standardui.treeview.domain;

public class TutorialTree extends com.cct.domain.Tree {

	private static final long serialVersionUID = 3523833997328694029L;

	private String groupSystem;
	private String operatorType;
	private String operatorLevel;
	private String listNo;
	private String url;
	private String systemName;
	private String menuName;
	private String functionName;
	private String path;

	public String getGroupSystem() {
		return groupSystem;
	}

	public void setGroupSystem(String groupSystem) {
		this.groupSystem = groupSystem;
	}

	public String getOperatorType() {
		return operatorType;
	}

	public void setOperatorType(String operatorType) {
		this.operatorType = operatorType;
	}

	public String getOperatorLevel() {
		return operatorLevel;
	}

	public void setOperatorLevel(String operatorLevel) {
		this.operatorLevel = operatorLevel;
	}

	public String getListNo() {
		return listNo;
	}

	public void setListNo(String listNo) {
		this.listNo = listNo;
	}

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	public String getSystemName() {
		return systemName;
	}

	public void setSystemName(String systemName) {
		this.systemName = systemName;
	}

	public String getMenuName() {
		return menuName;
	}

	public void setMenuName(String menuName) {
		this.menuName = menuName;
	}

	public String getFunctionName() {
		return functionName;
	}

	public void setFunctionName(String functionName) {
		this.functionName = functionName;
	}

	public String getPath() {
		return path;
	}

	public void setPath(String path) {
		this.path = path;
	}
}
